// Normal function
var sayHello = function (arg) {
    console.log(arg);
};
// Arrow function with param
var arrowFuncWithParam = function (arg) {
    console.log(arg);
    console.log(arg);
};
// Single line
var arrowFuncWithParamSingleLine = function (arg) { return console.log(arg); };
// without params 
var arrowWithoutParams = function () {
    console.log("Hello");
    console.log("Hello");
};
var arrowWithoutParamsSingle = function () { return console.log("Hello"); };
